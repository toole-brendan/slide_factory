def paint_mixed_series_legend(next_id) -> list[str]:
    """Left legend with one filled swatch and two short line-rule keys."""

    shapes: list[str] = []
    bar_entry = next(entry for entry in LEGEND_ENTRIES if entry.key_kind == "bar_swatch")
    shapes.append(
        _textbox(
            next_id(),
            "LegendBarSwatch",
            bar_entry.key_box,
            [_empty_centered_paragraph()],
            fill=bar_entry.color,
            line_color="none",
            anchor="ctr",
        )
    )

    for key in LEGEND_LINE_KEYS:
        shapes.append(
            connector(
                next_id(),
                "LegendLineRule",
                IN(key.x),
                IN(key.y),
                IN(key.w),
                IN(key.h),
                color=key.color,
                width=28575,
                dashed=True,
                arrow=True,
            )
        )

    for entry in LEGEND_ENTRIES:
        shapes.append(
            _textbox(
                next_id(),
                "LegendLabel",
                entry.label_box,
                [_one_line(entry.name)],
                fill=None,
                line_color="none",
                anchor="ctr",
                wrap="none",
                l_ins=0,
                t_ins=0,
                r_ins=0,
                b_ins=0,
            )
        )
    return shapes


