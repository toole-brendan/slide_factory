@dataclass(frozen=True)
class Box:
    """Geometry in inches; converted to EMU only at the primitive boundary."""

    x: float
    y: float
    w: float
    h: float

    def emu(self) -> tuple[int, int, int, int]:
        return IN(self.x), IN(self.y), IN(self.w), IN(self.h)


@dataclass(frozen=True)
class ComboSeries:
    """One semantic series in the native combo chart."""

    name: str
    chart_kind: str
    axis: str
    color: str
    values: tuple[float | int | None, ...]

    def data_dict(self) -> dict:
        """Readable data mirror used by audits and downstream tooling."""

        return {"name": self.name, "values": list(self.values)}

    def native_column_dict(self) -> dict:
        """Column-chart series contract for the fleet-additions bars."""

        return {
            "name": self.name,
            "values": list(self.values),
            "color": self.color,
            "hide_labels": True,
        }

    def native_line_overlay_dict(self, *, width: int = 28_575) -> dict:
        """Line-overlay series contract for the crude-price drivers."""

        return {
            "name": self.name,
            "values": list(self.values),
            "color": self.color,
            "width": width,
            "marker": "none",
            "smooth": False,
        }


@dataclass(frozen=True)
class AxisTitle:
    """Manual title placed outside the chart frame."""

    box: Box
    text: str
    align: str | None = None
    footnote: str | None = None


@dataclass(frozen=True)
class YearTick:
    """Manual x-axis anchor tick."""

    box: Box
    label: str


@dataclass(frozen=True)
class PeakAddLabel:
    """Manual label attached only to analytically important fleet-add peaks."""

    year: int
    box: Box
    label: str


@dataclass(frozen=True)
class LegendEntry:
    """Legend row for a mixed chart series."""

    name: str
    key_kind: str
    color: str
    key_box: Box
    label_box: Box


@dataclass(frozen=True)
class LineKey:
    """Short line-rule key used in the mixed-series legend."""

    name: str
    x: float
    y: float
    w: float
    h: float
    color: str


